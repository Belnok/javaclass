public class TernaryOp {

	
	// ternary operator 
	// ?:  condition  ? expression1: expression2
	public static void main(String[] args) {
		
		int i = 8;
		int j=0;
		
		j= i<6?1:2;
		
	/*	if (i>6)
			j = 1;
		else 
			j = 2;*/
		System.out.println(j);
		
	}

}

